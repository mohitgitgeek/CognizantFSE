-- 4. Functions
CREATE OR REPLACE FUNCTION CalculateAge(p_dob DATE) RETURN NUMBER IS BEGIN
  RETURN TRUNC(MONTHS_BETWEEN(SYSDATE,p_dob)/12); END;
/
CREATE OR REPLACE FUNCTION CalculateMonthlyInstallment(p_amount NUMBER,p_annual_rate NUMBER,p_years NUMBER) RETURN NUMBER IS
  r NUMBER:=p_annual_rate/1200; n NUMBER:=p_years*12;
BEGIN RETURN CASE WHEN r=0 THEN p_amount/n ELSE p_amount*r*POWER(1+r,n)/(POWER(1+r,n)-1) END; END;
/
CREATE OR REPLACE FUNCTION HasSufficientBalance(p_account_id NUMBER,p_amount NUMBER) RETURN BOOLEAN IS v_balance NUMBER;
BEGIN SELECT balance INTO v_balance FROM accounts WHERE accountid=p_account_id; RETURN v_balance>=p_amount;
EXCEPTION WHEN NO_DATA_FOUND THEN RETURN FALSE; END;
/

