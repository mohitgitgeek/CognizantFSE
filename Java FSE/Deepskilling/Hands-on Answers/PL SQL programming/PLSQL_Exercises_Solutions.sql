CREATE OR REPLACE TRIGGER LogTransaction AFTER INSERT ON transactions FOR EACH ROW BEGIN
  INSERT INTO auditlog(transaction_id,action,details) VALUES(:NEW.transactionid,'INSERT','Transaction '||:NEW.transactiontype||' amount '||:NEW.amount); END;
/
CREATE OR REPLACE TRIGGER CheckTransactionRules BEFORE INSERT ON transactions FOR EACH ROW DECLARE v_balance NUMBER;
BEGIN
  IF :NEW.transactiontype='Deposit' AND :NEW.amount<=0 THEN RAISE_APPLICATION_ERROR(-20002,'Deposit must be positive'); END IF;
  IF :NEW.transactiontype='Withdrawal' THEN SELECT balance INTO v_balance FROM accounts WHERE accountid=:NEW.accountid FOR UPDATE;
    IF :NEW.amount<=0 OR :NEW.amount>v_balance THEN RAISE_APPLICATION_ERROR(-20003,'Invalid withdrawal'); END IF; END IF;
END;
/

-- 6. Explicit cursors
DECLARE CURSOR GenerateMonthlyStatements IS SELECT c.name,t.transactionid,t.amount,t.transactiontype FROM transactions t JOIN accounts a ON a.accountid=t.accountid JOIN customers c ON c.customerid=a.customerid WHERE TRUNC(t.transactiondate,'MM')=TRUNC(SYSDATE,'MM');
BEGIN FOR x IN GenerateMonthlyStatements LOOP DBMS_OUTPUT.PUT_LINE(x.name||': '||x.transactiontype||' '||x.amount); END LOOP; END;
/
DECLARE CURSOR ApplyAnnualFee IS SELECT accountid FROM accounts FOR UPDATE; c_fee CONSTANT NUMBER:=100;
BEGIN FOR x IN ApplyAnnualFee LOOP UPDATE accounts SET balance=balance-c_fee,lastmodified=SYSDATE WHERE CURRENT OF ApplyAnnualFee; END LOOP; COMMIT; END;
/
DECLARE CURSOR UpdateLoanInterestRates IS SELECT loanid,interestrate FROM loans FOR UPDATE;
BEGIN FOR x IN UpdateLoanInterestRates LOOP UPDATE loans SET interestrate=x.interestrate-0.25 WHERE CURRENT OF UpdateLoanInterestRates; END LOOP; COMMIT; END;
/

