-- 2. Error handling
CREATE OR REPLACE PROCEDURE SafeTransferFunds(p_from NUMBER,p_to NUMBER,p_amount NUMBER) AS
  e_insufficient EXCEPTION; v_balance accounts.balance%TYPE;
BEGIN
  IF p_amount <= 0 THEN RAISE_APPLICATION_ERROR(-20001,'Amount must be positive'); END IF;
  SELECT balance INTO v_balance FROM accounts WHERE accountid=p_from FOR UPDATE;
  IF v_balance < p_amount THEN RAISE e_insufficient; END IF;
  UPDATE accounts SET balance=balance-p_amount,lastmodified=SYSDATE WHERE accountid=p_from;
  UPDATE accounts SET balance=balance+p_amount,lastmodified=SYSDATE WHERE accountid=p_to;
  COMMIT;
EXCEPTION WHEN e_insufficient THEN ROLLBACK; DBMS_OUTPUT.PUT_LINE('Transfer failed: insufficient funds');
  WHEN NO_DATA_FOUND THEN ROLLBACK; DBMS_OUTPUT.PUT_LINE('Transfer failed: account not found');
  WHEN OTHERS THEN ROLLBACK; DBMS_OUTPUT.PUT_LINE('Transfer failed: '||SQLERRM);
END;
/
CREATE OR REPLACE PROCEDURE UpdateSalary(p_employee_id NUMBER,p_percent NUMBER) AS
BEGIN
  UPDATE employees SET salary=salary*(1+p_percent/100) WHERE employeeid=p_employee_id;
  IF SQL%ROWCOUNT=0 THEN RAISE NO_DATA_FOUND; END IF; COMMIT;
EXCEPTION WHEN NO_DATA_FOUND THEN ROLLBACK; DBMS_OUTPUT.PUT_LINE('Employee '||p_employee_id||' does not exist');
  WHEN OTHERS THEN ROLLBACK; DBMS_OUTPUT.PUT_LINE(SQLERRM); END;
/
CREATE OR REPLACE PROCEDURE AddNewCustomer(p_id NUMBER,p_name VARCHAR2,p_dob DATE,p_balance NUMBER) AS
BEGIN
  INSERT INTO customers(customerid,name,dob,balance,lastmodified) VALUES(p_id,p_name,p_dob,p_balance,SYSDATE); COMMIT;
EXCEPTION WHEN DUP_VAL_ON_INDEX THEN ROLLBACK; DBMS_OUTPUT.PUT_LINE('Customer '||p_id||' already exists');
  WHEN OTHERS THEN ROLLBACK; DBMS_OUTPUT.PUT_LINE(SQLERRM); END;
/

