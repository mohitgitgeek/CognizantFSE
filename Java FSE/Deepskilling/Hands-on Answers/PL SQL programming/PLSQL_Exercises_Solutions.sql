CREATE OR REPLACE PACKAGE BODY EmployeeManagement AS
 PROCEDURE hire(p_id NUMBER,p_name VARCHAR2,p_position VARCHAR2,p_salary NUMBER,p_department VARCHAR2) AS BEGIN INSERT INTO employees VALUES(p_id,p_name,p_position,p_salary,p_department,SYSDATE); END;
 PROCEDURE update_employee(p_id NUMBER,p_position VARCHAR2,p_salary NUMBER) AS BEGIN UPDATE employees SET position=p_position,salary=p_salary WHERE employeeid=p_id; END;
 FUNCTION annual_salary(p_id NUMBER) RETURN NUMBER AS v NUMBER; BEGIN SELECT salary*12 INTO v FROM employees WHERE employeeid=p_id; RETURN v; END; END;
/
CREATE OR REPLACE PACKAGE AccountOperations AS PROCEDURE open_account(p_account NUMBER,p_customer NUMBER,p_type VARCHAR2,p_balance NUMBER); PROCEDURE close_account(p_account NUMBER); FUNCTION total_balance(p_customer NUMBER) RETURN NUMBER; END;
/
CREATE OR REPLACE PACKAGE BODY AccountOperations AS
 PROCEDURE open_account(p_account NUMBER,p_customer NUMBER,p_type VARCHAR2,p_balance NUMBER) AS BEGIN INSERT INTO accounts VALUES(p_account,p_customer,p_type,p_balance,SYSDATE); END;
 PROCEDURE close_account(p_account NUMBER) AS BEGIN DELETE FROM accounts WHERE accountid=p_account; END;
 FUNCTION total_balance(p_customer NUMBER) RETURN NUMBER AS v NUMBER; BEGIN SELECT NVL(SUM(balance),0) INTO v FROM accounts WHERE customerid=p_customer; RETURN v; END; END;
